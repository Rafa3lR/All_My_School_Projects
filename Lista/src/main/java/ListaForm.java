    public static void atualizar()
    {
        DefaultTableModel model = (DefaultTableModel) jTableLista.getModel();
        model.setRowCount(0);
        ListaDAO listaDAO = new ListaDAO();
        
        for (Lista lista: listaDAO.selectProductsMySQL())
        {
            model.addRow(new Object[]
                {
                    lista.getId(),
                    lista.getNome(),
                    lista.getQtde(),
                    lista.getPrice()
                });
            }
    }

