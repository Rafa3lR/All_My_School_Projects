    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ListaDAO cadastro = new ListaDAO();
        
        if (cadastro.cadastrarUsuario(jTextFieldUsuario.getText(), jPasswordFieldSenha.getText()))
        {
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            ListaForm lista = new ListaForm();
            lista.show();
            this.hide();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Usu√°rio ja existe!", "Erro!", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

