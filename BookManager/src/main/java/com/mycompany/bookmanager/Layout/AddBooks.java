    private void jButtonConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmActionPerformed
        if (MainClass.openEdit == 0)
        {
            String titulo = jTextFieldTitulo.getText();
            String autor = jTextFieldAutor.getText();
            MainClass.addBook(titulo, autor);
        }
        else
        {
            MainClass.books.get(MainClass.indexBook).setTitulo(jTextFieldTitulo.getText());
            MainClass.books.get(MainClass.indexBook).setAutor(jTextFieldAutor.getText());
            MainClass.booksLayoutList.get(MainClass.indexBook).jLabelTitulo.setText(jTextFieldTitulo.getText());
            MainClass.booksLayoutList.get(MainClass.indexBook).jLabelAutor.setText(jTextFieldAutor.getText());
            MainClass.openEdit = 0;
            
            Main.updatePanel(); 
        }
        
        this.dispose();
    }//GEN-LAST:event_jButtonConfirmActionPerformed

