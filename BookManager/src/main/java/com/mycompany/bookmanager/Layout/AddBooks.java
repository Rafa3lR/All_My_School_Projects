    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        if (MainClass.openEdit == 1)
        {
            jButtonDelete.setVisible(true);
            jTextFieldTitulo.setText(MainClass.books.get(MainClass.indexBook).getTitulo());
            jTextFieldAutor.setText(MainClass.books.get(MainClass.indexBook).getAutor());
        }
        else
        {
            jButtonDelete.setVisible(false);
        }
    }//GEN-LAST:event_formWindowOpened

    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        MainClass.openEdit = 0;
    }//GEN-LAST:event_formWindowClosed

    private void jButtonDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonDeleteMouseClicked
        Main.deletePanel(MainClass.booksLayoutList.get(MainClass.indexBook));
        MainClass.booksLayoutList.remove(MainClass.indexBook);
        MainClass.books.remove(MainClass.indexBook);
        this.dispose();
    }//GEN-LAST:event_jButtonDeleteMouseClicked

